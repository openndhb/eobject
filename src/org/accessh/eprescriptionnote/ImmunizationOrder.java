package org.accessh.eprescriptionnote;

public class ImmunizationOrder {
	private int immunizationPerformerIdentificationNumber;
	private int immunizationProductCode;
	
	public int getImmunizationPerformerIdentificationNumber() {
		return immunizationPerformerIdentificationNumber;
	}
	public void setImmunizationPerformerIdentificationNumber(int immunizationPerformerIdentificationNumber) {
		this.immunizationPerformerIdentificationNumber = immunizationPerformerIdentificationNumber;
	}
	public int getImmunizationProductCode() {
		return immunizationProductCode;
	}
	public void setImmunizationProductCode(int immunizationProductCode) {
		this.immunizationProductCode = immunizationProductCode;
	}
	

}
