package org.accessh.eprescriptionnote;

public class ActiveLabOrders {
	private String labOrderCode;
	private String labOrderDescription;
	
	public String getLabOrderCode() {
		return labOrderCode;
	}
	public void setLabOrderCode(String labOrderCode) {
		this.labOrderCode = labOrderCode;
	}
	public String getLabOrderDescription() {
		return labOrderDescription;
	}
	public void setLabOrderDescription(String labOrderDescription) {
		this.labOrderDescription = labOrderDescription;
	}

	
}
